    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         try
        {
            FileInputStream fin=new FileInputStream(filename);
            byte b[]=new byte[fin.available()];
            fin.read(b);
            Faculity_Interface mid=(Faculity_Interface)Naming.lookup("rmi://localhost/virtual");
            boolean b1=mid.uploadAssignment(fd.getFile(),b);
            if(b1)
            {
                JOptionPane.showMessageDialog(this,"Assignment Uploaded Successfully");
                jTextField1.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Error:Assignment did not upload");
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

